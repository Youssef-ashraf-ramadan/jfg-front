//Translation function
var languageData = {
  En: {
    dir: "ltr",

    lang: "en",
  },

  Ar: {
    dir: "rtl",

    lang: "ar",
  },
};
function updateLanguage(language) {
  var data = languageData[language];

  if (data) {
    document.documentElement.setAttribute("dir", data.dir);

    document.documentElement.setAttribute("lang", data.lang);
  }
}

function loadLanguageCSS(e) {
  const arFile = document.querySelector(".styleAr");

  if (e === "En") {
    arFile.disabled = true;
  } else if(e === 'Ar') {
    arFile.disabled = false;
  }else{
    arFile.disabled = true;
  }
}

function setLanguage(lang) {
  localStorage.setItem("language", lang);
}

 function translate(lang, langText) {
  document.body.style.display = "none";
  var elements = document.querySelectorAll("[data-i18n]");

  elements.forEach(function (element) {
    var key = element.getAttribute("data-i18n");
    element.classList.contains("input-Law")
      ? (element.placeholder = lang[key])
      : (element.textContent = lang[key]);
    if (element.type === "submit") element.value = lang[key];
  });

  // Swap CSS files
   loadLanguageCSS(langText);
  document.body.style.display = "block";
  // change dir
  updateLanguage(langText);
  // set localStorage
  setLanguage(langText);
}

//get language from localStorage
function getLanguage() {
  return localStorage.getItem("language");
}
window.addEventListener("load", function () {
  var language = getLanguage();

  if (language === "Ar") {
    // Load the page with the selected language
    translate(Ar, language);
  } else {
    // Load the page with the default language
    translate(En, language);
  }
});

